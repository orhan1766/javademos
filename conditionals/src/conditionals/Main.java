package conditionals;

public class Main {

	public static void main(String[] args) {
		
		int sayi = 19;
		
		if (sayi<20) {
			System.out.println("Sayı 20'den küçüktür.");
		} 
		
		if (sayi<30) {
			System.out.println("Sayı 30'dan küçüktür.");
		}
		
		if (sayi>20) {
			System.out.println("Sayı 20'den büyüktür.");
		} else {
			System.out.println("Sayı 20'den büyük değildir.");
		}
		
		if (sayi>19) {
			System.out.println("Sayı 19'dan büyüktür.");
		} else if(sayi==19) {
			System.out.println("Sayı 19'a eşittir.");
		} else {
			System.out.println("Sayı 19'dan küçüktür.");
		}
		
	}

}
